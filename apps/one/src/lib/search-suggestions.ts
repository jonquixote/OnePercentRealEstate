/**
 * Merge all suggestion sources and filter by query.
 * Returns up to `limit` results, prioritized: ZIP > city > state.
 */
export function getSuggestions(query: string, limit = 8): SearchSuggestion[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];

  const results: SearchSuggestion[] = [];

  // 1. ZIP codes (5-digit prefix match)
  if (/^\d{0,5}$/.test(q) && q.length > 0) {
    // Generate ZIP suggestions from common ranges
    const zipMatches = getZipSuggestions(q);
    results.push(...zipMatches);
  }

  // 2. City matches (starts-with, then includes)
  const cityStartsWith = CITIES.filter(c => c.label.toLowerCase().startsWith(q));
  const cityIncludes = CITIES.filter(c => c.label.toLowerCase().includes(q) && !c.label.toLowerCase().startsWith(q));
  results.push(...cityStartsWith, ...cityIncludes);

  // 3. State matches
  const stateMatches = STATES.filter(s =>
    s.label.toLowerCase().startsWith(q) || s.context?.toLowerCase() === q
  );
  results.push(...stateMatches);

  // Deduplicate by label+type
  const seen = new Set<string>();
  const deduped: SearchSuggestion[] = [];
  for (const r of results) {
    const key = `${r.label}::${r.type}`;
    if (!seen.has(key)) {
      seen.add(key);
      deduped.push(r);
    }
    if (deduped.length >= limit) break;
  }

  return deduped;
}

